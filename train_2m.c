static int fr(const int*let,int n){
    if(n<2)return -1;
    int ps=sp(let,n);const int*s=let+ps;int sn=n-ps-ss(let+ps,n-ps);
    if(sn>=2){int best=-1,bsp=999;
        for(int ri=0;ri<nroots;ri++){int t[3]={roots[ri].c[0],roots[ri].c[1],roots[ri].c[2]};
            int p[3]={-1,-1,-1};int ti=0;for(int i=0;i<sn&&ti<3;i++)if(s[i]==t[ti]){p[ti]=i;ti++;}
            if(ti==3){int sp2=p[2]-p[0];if(sp2<bsp){bsp=sp2;best=ri;}}}
        if(best>=0)return best;}
    /* No fallback — unknown words skipped, not turned into garbage roots */
    return -1;
}
static int extract(const char*text,int*out,int mx){
    const unsigned char*p=(const unsigned char*)text;
    int n=0; char wb_utf8[128]; int wb[64],wn=0,wp=0;
    while(*p&&n<mx){
        if(isheb(p)){
            if(wn<64)wb[wn++]=u8let(p);
            if(wp<126){wb_utf8[wp++]=p[0];wb_utf8[wp++]=p[1];}
            p+=2;
        } else {
            if(wn>0){
                wb_utf8[wp]=0;
                /* Try function word first */
                int fid=func_match(wb_utf8);
                if(fid>=0){out[n++]=fid;}
                else{int r=fr(wb,wn);if(r>=0)out[n++]=r;}
                wn=0;wp=0;
            }
            p++;
        }
    }
    if(wn>0){wb_utf8[wp]=0;int fid=func_match(wb_utf8);
        if(fid>=0)out[n++]=fid;
        else{int r=fr(wb,wn);if(r>=0)out[n++]=r;}}
    return n;
}

